/* Multiboot1 header + entry point. Boots in 32-bit protected mode (GRUB/QEMU). */
.set MAGIC,    0x1BADB002
.set FLAGS,    0x0
.set CHECKSUM, -(MAGIC + FLAGS)

.section .multiboot
.align 4
.long MAGIC
.long FLAGS
.long CHECKSUM

.section .bss
.align 16
stack_bottom:
.skip 16384
stack_top:

.section .text
.global _start
.type _start, @function
_start:
    mov $stack_top, %esp
    call kernel_main
    cli
1:  hlt
    jmp 1b
